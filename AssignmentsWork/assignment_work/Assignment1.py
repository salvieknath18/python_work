n = int(input("Enter number n : "))

# Printing n natural numbers
for i in range(n):
    print(i+1)

# Printing n natural numbers in revers way
for i in range(n):
    print(n-i)

# Printing characters a to z
for i in range(97, (97+26)):
    print(chr(i))

# Printing n even numbers 0 to 10
for i in range(0, 100, 2):
    print(i)

# Printing n odd numbers 0 to 100
for i in range(1, 100, 2):
    print(i)
